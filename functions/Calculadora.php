<?php

function somar($n1,$n2){
    return $n1 + $n2;
}

function subtrair($n1,$n2){
    return $n1 - $n2;
}

function multiplicar($n1,$n2){
    return $n1 * $n2;
}

function dividir($n1,$n2){
    return $n1 / $n2;
}

function potencia($n1,$n2){
    return pow($n1, $n2);
}

function modulo($n1,$n2){
    return $n1 % $n2;
}